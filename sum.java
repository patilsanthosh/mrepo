class sum
{
	public static void main(String args[])
	{
	int a=2;
	int b=3;
	int sum=0;
	int sub=0;
	int mul=0;
	int div=0;
	sum=a+b;
	sub=a-b;
	mul=a*b;
	div=a/b;
	//System.out.println("sum of a & b")+(5);
	System.out.println("sum of "+ a +" & " + b +" = " +sum);
	System.out.println("sub of "+ a +" & " + b +" = " +sub);
	System.out.println("mul of "+ a +" & " + b +" = " +mul);
	System.out.println("div of "+ a +" & " + b +" = " +div);
	}
}	

